// 가이드 "웹보기" 링크 (Docs → view 모드)
function getGuideViewLink_(){
  var id = (CONFIG && CONFIG.GUIDE_FILE_ID) ? String(CONFIG.GUIDE_FILE_ID).trim() : '';
  return id ? ('https://docs.google.com/document/d/' + id + '/view') : '';
}

